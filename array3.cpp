#include <iostream>
using namespace std;
int main()
{
    int numero[20];
    int sumatoria = 0;
    cout << "Sumatoria de los numeros del 1 al 100\n";

    for (int i = 1; i<21; i++ )
    {
        numero[i];
        cout << numero[i] << "\n";
        sumatoria= sumatoria +numero[i];
    }
    cout << sumatoria;
    return 0;
}